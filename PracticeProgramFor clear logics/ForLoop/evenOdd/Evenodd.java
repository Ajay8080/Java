/*Print even and odd number between 1
100 seperately by using for loop? */

public class Evenodd{
    public static void main(String[] args) {
        for(int i=1;i<=100;i++){
            if(i%2==0){
                System.out.println("Even number is "+i);
                }
                else{
                    System.out.println("Odd number is "+i);
                    }
                }
    }
            
}