class Emp{
    private String name;
    private int number;
    private double salary;

    public Emp(String name, int number, double salary){
        this.name=name;
        this.number=number;
        this.salary=salary;
    }
    public String getName(){
        return name;
    }
    public int getNumber(){
        return number;
    }
    public double getSalary(){
        return salary;

        
    }
}