// Implicit Coversion byte to short, int, long, float, or double

public class Byte {
    public static void main(String[] args) {
        byte b = 10;
        short s = b;
        int i = b;
        long l = b;
        float f = b;
        double d = b;

        System.out.println("Value for Byte is = "+b);
        System.out.println("Value for short is = "+s);
        System.out.println("Value for int is = "+i);
        System.out.println("Value for long is = "+l);
        System.out.println("Value for float is = "+f);
        System.out.println("Value for double is = "+d);
        
    }
    
}
